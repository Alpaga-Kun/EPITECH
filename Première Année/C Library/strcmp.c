#include "library.h"

int sstrcmp(char const *s1, char const *s2)
{
    for (; (*s1 != '\0' && *s2 != '\0') && *s1 == *s2; s1++, s2++);
    return (*s1 == *s2 ? 0 : *s1 > *s2 ? 1 : -1);
}