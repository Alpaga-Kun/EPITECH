#include "library.h"

int sstrncmp(char const *s1, char const *s2, size_t n)
{
    for (; (*s1 != n && *s2 != n) && *s1 == *s2; s1++, s2++);
    return (*s1 == *s2 ? 0 : *s1 > *s2 ? 1 : -1);
}